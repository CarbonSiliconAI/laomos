function getSection(pathname: string): string {
    if (pathname.startsWith('/operations')) return 'operations';
    if (pathname.startsWith('/workforce')) return 'workforce';
    if (pathname.startsWith('/knowledge')) return 'knowledge';
    if (pathname.startsWith('/governance')) return 'governance';
    return 'home';
}

export default function Sidebar() {
    const location = useLocation();
    const sectionKey = getSection(location.pathname);
    const section = SECTIONS[sectionKey];

    if (sectionKey === 'home' || !section?.items?.length) return null;

    return (
        <aside className="sidebar">
            <div className="sidebar__header">
                <span className="sidebar__title">{section.title}</span>
            </div>
            <nav className="sidebar__nav">
                {section.items.map((item) => (
                    <NavLink
                        key={item.path}
                        to={item.path}
                        end={item.path === `/${sectionKey}`}
                        className={({ isActive }) =>
                            `sidebar__item ${isActive ? 'sidebar__item--active' : ''}`
                        }
                    >
                        {item.icon && (
                            <span className="sidebar__item-icon">{item.icon}</span>
                        )}
                        <span className="sidebar__item-label">{item.label}</span>
                    </NavLink>
                ))}
            </nav>
        </aside>
    );
}
